#!/usr/bin/env node
/**
 * dev-coding-task вЂ” End-to-end coding task via AgentHarness + Grok OAuth.
 *
 * Runs a single coding task that exercises the full tool loop:
 *   1. Grok receives the prompt + tool catalog
 *   2. Grok decides to call write_file в†’ AgentHarness executes в†’ returns result
 *   3. Grok decides to call read_file в†’ AgentHarness executes в†’ returns result
 *   4. Grok produces a final summary message
 *
 * Working dir is /tmp/zelari-coding-test (created if missing, isolated
 * from the project).
 */

import { mkdirSync, rmSync, existsSync, readFileSync } from 'node:fs';
import { join } from 'node:path';

import { AgentHarness } from '../dist/main/core/AgentHarness.js';
import { createBuiltinToolRegistry } from '../dist/cli/toolRegistry.js';
import { openaiCompatibleProvider, providerConfigFor } from '../dist/cli/provider/openai-compatible.js';

// в”Ђв”Ђ Setup в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
const WORKDIR = '/tmp/zelari-coding-test';
if (existsSync(WORKDIR)) {
  rmSync(WORKDIR, { recursive: true, force: true });
}
mkdirSync(WORKDIR, { recursive: true });
process.chdir(WORKDIR);

const taskPrompt = process.argv.slice(2).join(' ') ||
  `Create a file called index.html in the current working directory with the following content:
- A <!DOCTYPE html> document
- <html lang="en"> with <head> containing <title>Zelari Code Test</title>
- <body> containing an <h1>Zelari Code works</h1>, a <p> paragraph explaining that this file was generated by the Zelari Code CLI as an end-to-end smoke test, and a <a href="https://github.com/N-THEM-Studio/zelari-code">link to the repo</a>
Then use read_file to verify the file was written correctly, and end your reply with a one-line summary.`;

// в”Ђв”Ђ Resolve Grok config + build harness в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
const providerId = 'grok';
const providerCfg = await providerConfigFor(providerId);
if (!providerCfg) {
  console.error(`\x1b[31mвњ— No config for provider "${providerId}". Run /login grok first.\x1b[0m`);
  process.exit(1);
}
console.log(`\x1b[36m[setup]\x1b[0m Provider: ${providerCfg.providerId}  Model: ${providerCfg.model}  baseUrl: ${providerCfg.baseUrl}`);

const providerStream = openaiCompatibleProvider(providerCfg);
const { registry: toolRegistry, tools } = createBuiltinToolRegistry({ root: WORKDIR });
const toolList = tools.map((t) => `- ${t.name}: ${t.description}`).join('\n');

const systemPrompt = `You are Zelari Code, an interactive AI coding agent running in a test harness.
You have real filesystem tools. Use them. Working directory: ${WORKDIR}

Available tools:
${toolList}

When done, give a one-line summary.`;

console.log(`\x1b[36m[setup]\x1b[0m Tools registered: ${tools.map(t => t.name).join(', ')}`);
console.log(`\x1b[36m[setup]\x1b[0m Working dir: ${WORKDIR}`);
console.log(`\x1b[36m[setup]\x1b[0m Task: ${taskPrompt.slice(0, 100)}${taskPrompt.length > 100 ? 'вЂ¦' : ''}\n`);

const harness = new AgentHarness({
  model: providerCfg.model,
  provider: providerCfg.providerId,
  messages: [
    { role: 'system', content: systemPrompt },
    { role: 'user', content: taskPrompt },
  ],
  tools: toolRegistry.toOpenAITools().map((t) => ({
    name: t.function.name,
    description: t.function.description,
    parameters: t.function.parameters,
  })),
  toolRegistry,
  providerStream,
});

// в”Ђв”Ђ Stream events в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
const ts = () => new Date().toISOString().slice(11, 23);
let textBuf = '';
let toolCallCount = 0;
let totalUsage = null;
const startMs = Date.now();

for await (const event of harness.run()) {
  switch (event.type) {
    case 'agent_start':
      console.log(`\x1b[34m[${ts()}]\x1b[0m \x1b[1magent_start\x1b[0m  session=${event.sessionId}  model=${event.model ?? providerCfg.model}`);
      break;
    case 'message_start':
      console.log(`\x1b[34m[${ts()}]\x1b[0m message_start  messageId=${event.messageId}`);
      textBuf = '';
      break;
    case 'message_delta':
      textBuf += event.delta;
      process.stdout.write(event.delta);
      break;
    case 'thinking_delta':
      process.stdout.write(`\x1b[2m${event.delta}\x1b[0m`); // dimmed
      break;
    case 'message_end': {
      const usage = event.usage;
      if (usage) totalUsage = usage;
      console.log(); // newline after streaming
      console.log(`\x1b[34m[${ts()}]\x1b[0m message_end    reason=${event.finishReason}  length=${event.totalLength}  usage=${usage ? `${usage.totalTokens}tok (${usage.promptTokens}+${usage.completionTokens})` : 'n/a'}`);
      break;
    }
    case 'tool_execution_start': {
      toolCallCount++;
      const argsStr = JSON.stringify(event.args).slice(0, 140);
      console.log(`\n\x1b[33m[${ts()}]\x1b[0m \x1b[1mв†’ tool_call #${toolCallCount}\x1b[0m  ${event.toolName}(${argsStr}${argsStr.length >= 140 ? 'вЂ¦' : ''})`);
      break;
    }
    case 'tool_execution_end': {
      const preview = (event.result || '').replace(/\n/g, ' вЏЋ ').slice(0, 180);
      console.log(`\x1b[33m[${ts()}]\x1b[0m в†ђ tool_result ${event.isError ? '\x1b[31mвњ—\x1b[0m' : '\x1b[32mвњ“\x1b[0m'}  ${event.durationMs}ms  ${preview}${preview.length >= 180 ? 'вЂ¦' : ''}`);
      break;
    }
    case 'agent_end': {
      const elapsed = ((Date.now() - startMs) / 1000).toFixed(1);
      console.log(`\n\x1b[1m\x1b[36m[${ts()}]\x1b[0m \x1b[1magent_end\x1b[0m  duration=${elapsed}s  tool_calls=${toolCallCount}`);
      if (totalUsage) {
        console.log(`\x1b[36m[usage]\x1b[0m prompt=${totalUsage.promptTokens}  completion=${totalUsage.completionTokens}  total=${totalUsage.totalTokens}`);
      }
      break;
    }
    case 'error':
      console.error(`\n\x1b[31m[${ts()}]\x1b[0m ERROR: ${event.message}`);
      break;
    default:
      console.log(`\x1b[34m[${ts()}]\x1b[0m ${event.type}`);
  }
}

// в”Ђв”Ђ Post-check в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
const indexPath = join(WORKDIR, 'index.html');
console.log(`\n\x1b[1m\x1b[36mв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђ\x1b[0m`);
console.log(`\x1b[1m\x1b[36m  POST-RUN VERIFICATION\x1b[0m`);
console.log(`\x1b[1m\x1b[36mв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђ\x1b[0m`);
if (existsSync(indexPath)) {
  const content = readFileSync(indexPath, 'utf8');
  console.log(`\x1b[32mвњ“\x1b[0m ${indexPath} exists (${content.length} bytes)`);
  console.log('---');
  console.log(content);
  console.log('---');
  const checks = [
    ['<!DOCTYPE html>', '<!DOCTYPE html>'],
    ['<title>Zelari Code Test</title>', '<title>Zelari Code Test</title>'],
    ['<h1>Zelari Code works</h1>', '<h1>Zelari Code works</h1>'],
    ['GitHub link', 'github.com/N-THEM-Studio/zelari-code'],
  ];
  for (const [name, needle] of checks) {
    const present = content.includes(needle);
    console.log(`  ${present ? '\x1b[32mвњ“\x1b[0m' : '\x1b[31mвњ—\x1b[0m'} ${name}`);
  }
} else {
  console.log(`\x1b[31mвњ—\x1b[0m ${indexPath} does NOT exist вЂ” agent did not create the file`);
  process.exit(1);
}