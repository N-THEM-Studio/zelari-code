function createDocumentStub(ctx: WorkspaceContext): EnhancedToolDefinition {
  return {
    name: "createDocument",
    description:
      "Create a Markdown document in the project docs (README/CHANGELOG/spec draft produced by the council).",
    category: "vault",
    parameters: [],
    execute: async (args) => {
      return workspaceMutex.run(`${ctx.rootDir}:doc`, () => {
        const title = (args["title"] as string) ?? "New Document";
        const content = (args["content"] as string) ?? "";
        const tags = (args["tags"] as string[]) ?? [];
        // v0.7.8: models routinely pass the FILENAME as title ("risks.md",
        // "synthesis.md"), which slugified to "risks-md"/"synthesis-md" and
        // produced duplicated artifacts (docs/risks-md.md next to risks.md,
        // HANDOFF 2026-07-03 §5.1). Strip the extension before slugifying.
        const normalizedTitle = title.replace(/\.(md|markdown)\s*$/i, "");
        const slug = slugify(normalizedTitle) || `doc-${Date.now()}`;

        if (slug === "risks") {
          const risksPath = workspaceFile(ctx.rootDir, "risks");
          const riskMeta: RiskFrontmatter = {
            kind: "risk",
            id: "risks",
            severity: "medium",
            date: new Date().toISOString().slice(0, 10),
          };
          ctx.storage.write(risksPath, riskMeta, content);
          return `Document "${title}" created at risks.md (workspace root).`;
        }

        const path = workspaceArtifact(ctx.rootDir, "docs", slug);
        const meta: DocFrontmatter = {
          kind: "doc",
          id: slug,
          date: new Date().toISOString().slice(0, 10),
          tags,
        };
        ctx.storage.write(path, meta, content);
        return `Document "${title}" created at docs/${slug}.md.`;
      });
    },
  };
}

/**
 * After this many searchDocuments calls in one council run, every further
 * result carries a nudge to stop searching and act. Live test 2026-07-02:
 * one member burned ~25 near-identical searches (each phrased slightly
 * differently, so the duplicate-call cache never tripped) before doing any
 * actual work.
 */
const SEARCH_NUDGE_THRESHOLD = 5;

function searchDocumentsStub(ctx: WorkspaceContext): EnhancedToolDefinition {
  let callCount = 0;
  return {
    name: "searchDocuments",
    description:
      "Search all workspace documents (`.zelari/**`) for a keyword. Returns matching files with snippets. " +
      "Search AT MOST 2-3 times, then act on the results: read the matched files with read_file instead of rephrasing the same search.",
    category: "analysis",
    parameters: [],
    execute: async (args) => {
      callCount++;
      const nudge =
        callCount > SEARCH_NUDGE_THRESHOLD
          ? `[note] This is searchDocuments call #${callCount} in this run — STOP searching. Read the files you already found with read_file and act on them.\n\n`
          : "";
      const rawQuery = ((args["query"] as string) ?? "").trim();
      const limit = (args["limit"] as number) ?? 5;
      if (!rawQuery) return "searchDocuments requires a query.";
      // v0.7.3: models routinely write boolean-ish queries ("plan OR phase OR
      // milestone"). The previous implementation substring-matched the WHOLE
      // query string, so every OR query — and any multi-word query that didn't
      // appear verbatim — returned "No matches" even for documents created
      // seconds earlier. Match any OR-phrase first, then fall back to
      // any-word matching per file.
      const phrases = rawQuery
        .toLowerCase()
        .split(/\s+or\s+/i)
        .map((p) => p.trim())
        .filter((p) => p.length > 0);
      const words = rawQuery
        .toLowerCase()
        .split(/[^a-z0-9_-]+/)
        .filter(
          (w) => w.length >= 3 && w !== "or" && w !== "and" && w !== "the",
        );
      const files = [
        ...ctx.storage.listMarkdown(join(ctx.rootDir, "decisions")),
        ...ctx.storage.listMarkdown(join(ctx.rootDir, "docs")),
        ...ctx.storage.listMarkdown(join(ctx.rootDir, "reviews")),
        ...ctx.storage.listMarkdown(join(ctx.rootDir, "plan-tasks")),
        ...ctx.storage.listMarkdown(join(ctx.rootDir, "milestones")),
        workspaceFile(ctx.rootDir, "plan"),
        workspaceFile(ctx.rootDir, "risks"),
      ];
      const results: { path: string; snippet: string }[] = [];
      for (const file of files) {
        if (!existsSync(file)) continue;
        const raw = readFileSync(file, "utf8");
        const content = raw.toLowerCase();
        let idx = -1;
        let matchLen = 0;
        for (const p of phrases) {
          const i = content.indexOf(p);
          if (i >= 0) {
            idx = i;
            matchLen = p.length;
            break;
          }
        }
        if (idx < 0) {
          for (const w of words) {
            const i = content.indexOf(w);
            if (i >= 0) {
              idx = i;
              matchLen = w.length;
              break;
            }
          }
        }
        if (idx < 0) continue;
        const start = Math.max(0, idx - 40);
        const end = Math.min(raw.length, idx + matchLen + 40);
        const snippet =
          "…" + raw.slice(start, end).replace(/\n/g, " ").trim() + "…";
        results.push({
          path: relative(ctx.rootDir, file) || basename(file),
          snippet,
        });
        if (results.length >= limit) break;
      }
      if (results.length === 0)
        return `${nudge}No matches for "${rawQuery}" in workspace.`;
      return (
        nudge + results.map((r) => `[${r.path}]\n${r.snippet}`).join("\n\n")
      );
    },
  };
}

function linkDocumentsStub(ctx: WorkspaceContext): EnhancedToolDefinition {
  return {
    name: "linkDocuments",
    description:
      "Link two workspace artifacts. Adds the `related:` field to the source frontmatter so backreferences can be traced.",
    category: "vault",
    parameters: [],
    execute: async (args) => {
      return workspaceMutex.run(`${ctx.rootDir}:link`, () => {
        // v0.7.8: the provider-facing JSON schema (toolSchemas.ts) advertises
        // sourceId/targetPathOrTitle — accept those aliases too, otherwise
        // every schema-conformant call fails with "requires fromId and toId".
        const fromId =
          (args["fromId"] as string) ?? (args["sourceId"] as string);
        const toId =
          (args["toId"] as string) ??
          (args["targetId"] as string) ??
          (args["targetPathOrTitle"] as string);
        if (!fromId || !toId) return "linkDocuments requires fromId and toId.";
        // Find the source file by id
        const allFiles = [
          ...ctx.storage.listMarkdown(join(ctx.rootDir, "decisions")),
          ...ctx.storage.listMarkdown(join(ctx.rootDir, "docs")),
          ...ctx.storage.listMarkdown(join(ctx.rootDir, "reviews")),
        ];
        const source = allFiles.find((f) => {
          try {
            const meta = ctx.storage.read<{ id: string }>(f).meta;
            return meta.id === fromId;
          } catch {
            return false;
          }
        });
        if (!source) return `Source "${fromId}" not found.`;
        const doc = ctx.storage.read<
          AdrFrontmatter | DocFrontmatter | ReviewFrontmatter
        >(source);
        const related = doc.meta.related ?? [];
        if (!related.includes(toId)) related.push(toId);
        doc.meta.related = related;
        ctx.storage.write(source, doc.meta, doc.body);
        return `Linked ${fromId} → ${toId}.`;
      });
    },
  };
}

function getDocumentBacklinksStub(
  ctx: WorkspaceContext,
): EnhancedToolDefinition {
  return {
    name: "getDocumentBacklinks",
    description:
      "List all workspace artifacts that link to the given artifact via their `related:` frontmatter.",
    category: "analysis",
    parameters: [],
    execute: async (args) => {
      // v0.7.8: the provider-facing JSON schema advertises `id` — accept it.
      const targetId = (args["targetId"] as string) ?? (args["id"] as string);
      if (!targetId) return "getDocumentBacklinks requires targetId.";
      const allFiles = [
        ...ctx.storage.listMarkdown(join(ctx.rootDir, "decisions")),
        ...ctx.storage.listMarkdown(join(ctx.rootDir, "docs")),
        ...ctx.storage.listMarkdown(join(ctx.rootDir, "reviews")),
      ];
      const backlinks: string[] = [];
      for (const file of allFiles) {
        try {
          const doc = ctx.storage.read<{ id: string; related?: string[] }>(
            file,
          );
          if (doc.meta.id === targetId) continue;
          if ((doc.meta.related ?? []).includes(targetId)) {
            backlinks.push(doc.meta.id);
          }
        } catch {
          /* skip */
        }
      }
      if (backlinks.length === 0) return `No backlinks to "${targetId}".`;
      return backlinks.map((id) => `- ${id}`).join("\n");
    },
  };
}

