function updateTaskStub(ctx: WorkspaceContext): EnhancedToolDefinition {
  return {
    name: "updateTask",
    description: "Update a task's status (pending|in_progress|done|blocked).",
    category: "project",
    parameters: [],
    execute: async (args) => {
      return workspaceMutex.run(`${ctx.rootDir}:plan`, () => {
        const taskId = args["taskId"] as string;
        const rawStatus = args["status"] as string;
        if (!taskId || !rawStatus)
          return "updateTask requires taskId and status.";
        const valid = ["pending", "in_progress", "done", "blocked"];
        // v0.7.3: models routinely use kanban vocabulary ("todo", "completed")
        // — map the common synonyms instead of erroring on each attempt.
        const aliases: Record<string, string> = {
          todo: "pending",
          "to-do": "pending",
          open: "pending",
          backlog: "pending",
          "in-progress": "in_progress",
          "in progress": "in_progress",
          doing: "in_progress",
          started: "in_progress",
          active: "in_progress",
          complete: "done",
          completed: "done",
          finished: "done",
          closed: "done",
          resolved: "done",
          stuck: "blocked",
          waiting: "blocked",
          "on-hold": "blocked",
          "on hold": "blocked",
        };
        const status = valid.includes(rawStatus)
          ? rawStatus
          : aliases[rawStatus.toLowerCase()];
        if (!status)
          return `Invalid status: ${rawStatus}. Must be one of: ${valid.join(", ")}.`;
        const summary = readPlan(ctx);
        const task = summary.tasks.find((t) => t.id === taskId);
        if (!task) return `Task "${taskId}" not found.`;
        task.status = status as "pending" | "in_progress" | "done" | "blocked";
        writePlan(ctx, summary);
        return `Task "${taskId}" updated to status="${status}".`;
      });
    },
  };
}

